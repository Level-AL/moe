package day06_operators;

public class AssignmentOperators {

	public static void main(String[] args) {

		// = , +=, -=, *=, /=
		
		int a = 3;
		int b = 5;
		
		a += b; // a = a + b = 
		System.out.println(a);
		
		a -= 4;
		
		System.out.println(a);
		
		a *= b; 
		
		System.out.println(a);
		
		a /= 2;
		
		System.out.println(a );
		
	
	
	
	
	}

}
